(function (root) {
    var sd = root.sd = {};

    var DIFFICULTY = {
        "easy": 19,
        "medium": 28,
        "hard": 37,
        "very-hard": 46,
        "insane": 55,
        "inhuman": 64,
        "impossible": 73
    };

    // Sudoku solver
    sd.solve = function (board, max, rnd) {
        let p = {
            board: this.copyBoard(board),
            solutions: [],
            max: max ?? 20,
            nc: [9, 9, 9, 9, 9, 9, 9, 9, 9, 9],
            emptySpot: [],
            emptySpotIdx: 0,
            rnd: rnd ?? false,
        }

        board.forEach((row, rowIndex) =>
            row.forEach((num, colIndex) => {
                if (num !== 0) {
                    p.nc[num]--;
                } else {
                    p.emptySpot.push([rowIndex, colIndex]);
                }
            })
        );

        this.attemptSolve(p);
        return p.solutions;
    };

    sd.attemptSolve = function (p) {
        if (p.emptySpotIdx >= p.emptySpot.length) {
            p.solutions.push(this.copyBoard(p.board));
            return true; // All spots filled, puzzle solved
        }

        let [row, col] = p.emptySpot[p.emptySpotIdx++];
        let start = p.rnd ? Math.floor(Math.random() * 10) : 0;
        for (let i = 0; i < 9; i++) {
            let num = (start + i) % 9 + 1;
            if (--p.nc[num] >= 0 && this.isValid(p.board, row, col, num)) {
                p.board[row][col] = num;
                if (this.attemptSolve(p)) {
                    if (p.solutions.length >= p.max) {
                        p.nc[num]++;
                        return true;
                    }
                }
                p.board[row][col] = 0;
            }
            p.nc[num]++;
        }
        p.emptySpotIdx--;
        return false; // No valid number found
    };

    // 将输入的数独数据进行校验
    sd.check = function (base, r, c, num) {
        if (!this.isValid(base, r, c, num)) {
            return false;
        }

        var d = this.copyBoard(base);
        d[r][c] = num;

        return this.solve(d, 1).length > 0;
    };

    sd.isValid = function (board, row, col, num) {
        return (
            this.isRowValid(board, row, num) &&
            this.isColValid(board, col, num) &&
            this.isBoxValid(board, row - (row % 3), col - (col % 3), num)
        );
    };

    sd.isRowValid = function (board, row, num) {
        return board[row].every(colValue => colValue !== num);
    };

    sd.isColValid = function (board, col, num) {
        return board.every(row => row[col] !== num);
    };

    sd.isBoxValid = function (board, startRow, startCol, num) {
        for (let row = 0; row < 3; row++) {
            for (let col = 0; col < 3; col++) {
                if (board[row + startRow][col + startCol] === num) {
                    return false;
                }
            }
        }
        return true;
    };

    sd.findEmptySpot = function (board) {
        for (let row = 0; row < 9; row++) {
            for (let col = 0; col < 9; col++) {
                if (board[row][col] === 0) {
                    return [row, col];
                }
            }
        }
        return null;
    };

    sd.copyBoard = function (board) {
        return board.map(row => row.slice());
    };

    sd.strToBoard = function (str) {
        return Array.from({ length: 9 }, (_, i) =>
            Array.from({ length: 9 }, (_, j) => parseInt(str[i * 9 + j]))
        );
    };

    sd.boardToStr = function (board) {
        return board.flatMap(row => row.join('')).join('');
    };

    sd.randomBoard = function () {
        let board = Array.from({ length: 9 }, () => Array(9).fill(0));
        let s = this.solve(board, 1, true);
        return s ? s[0] : nullptr;
    };

    sd.generate = function (difficulty) {
        if (typeof difficulty === "string" || typeof difficulty === "undefined") {
            difficulty = DIFFICULTY[difficulty] || DIFFICULTY.easy;
        }

        // generate a random board
        board = this.randomBoard();

        let grid = [...Array(81).keys()];
        let hole = 0;
        while (grid.length > 0) {
            // random dig
            let i = Math.floor(Math.random() * grid.length);
            let row = Math.floor(grid[i] / 9);
            let col = grid[i] % 9;
            grid.splice(i, 1);

            let num = board[row][col];
            board[row][col] = 0;
            hole++;

            // check
            let s = this.solve(board, 2, false);
            if (s && s.length == 1) { // only one solution
                if (hole >= difficulty) {
                    break;
                }
                continue;
            }

            // can not dig here
            board[row][col] = num;
            hole--;
        }
        return board;
    };
})(this);

class Cell {
    constructor(row, col, num) {
        this.orgNum = num ?? 0;
        if (this.orgNum < 0 || this.orgNum > 9) {
            this.orgNum = 0;
        }
        this.row = row;
        this.col = col;
        this.value = this.orgNum;
        this.sure = false;
        this.pencil = new Array(10).fill(false);

        this.cell = document.getElementById(`r${row}c${col}`);
        if (this.orgNum > 0 && this.orgNum < 10) {
            this.cell.className = 'game-cell gv';
        }
        else {
            this.cell.className = 'game-cell';
        }

        this.render();
    }

    dump() {
        return {
            row: this.row,
            col: this.col,
            value: this.value,
            orgNum: this.orgNum,
            sure: this.sure,
            pencil: this.pencil,
            className: this.cell.className
        };
    }

    restore(data) {
        this.row = data.row;
        this.col = data.col;
        this.value = data.value;
        this.orgNum = data.orgNum;
        this.sure = data.sure;
        this.pencil = data.pencil;
        this.cell = document.getElementById(`r${this.row}c${this.col}`);
        this.cell.className = data.className;
        this.render();
    }

    render() {
        if (this.value == 0) {
            var html = '<div class="cell-value"></div>';
            var pencilHtml = `<div class="pencil-grid${this.sure ? ' sure' : ''}">`;
            var pencilCount = 0;
            for (var i = 1; i < 10; i++) {
                pencilHtml += `<div class="pencil-grid-cell pgc-${i}">`;
                if (this.pencil[i]) {
                    pencilCount++;

                    pencilHtml += '<svg width="20" height="31" viewBox="0 0 20 31.4">';
                    pencilHtml += `<path d="${numPath[i]}"></path>`;
                    pencilHtml += '</svg>';
                }
                pencilHtml += '</div>';
            }
            pencilHtml += '</div>'
            if (pencilCount > 0) {
                html += pencilHtml;
            }
            this.cell.innerHTML = html;
        }
        else {
            var html = '<div class="cell-value">';
            html += '<svg width="23" height="32" viewBox="0 0 23 32">';
            html += `<path d="${numPath[this.value]}"></path>`;
            html += '</svg>';
            html += '</div>';
            this.cell.innerHTML = html;
        }
    }

    addClass(className) {
        if (!this.cell.classList.contains(className)) {
            this.cell.classList.add(className);
        }
    }

    removeClass(className) {
        this.cell.classList.remove(className);
    }

    setHighLight(isSet, type) {
        var className = 'cell-tips';
        if (type == 2) {
            className = 'cell-tips-same';
        }
        else if (type == 3) {
            className = 'cell-err';
        }

        if (isSet) {
            this.addClass(className);
        }
        else {
            this.removeClass(className);
        }
    }

    setPencilHighLight(num) {
        for (var i = 1; i < 10; i++) {
            if (i == num) {
                this.addClass(`pencil-light${i}`);
            }
            else {
                this.removeClass(`pencil-light${i}`);
            }
        }
    }

    setSelected(selected) {
        if (selected) {
            this.addClass('cell-selected');
        }
        else {
            this.removeClass('cell-selected');
        }
    }

    isSelected() {
        return true == this.cell.classList.contains('cell-selected');
    }

    setValue(num) {
        if (this.orgNum > 0) {
            // 有初始值不能修改
            return;
        }

        if (num < 0 || num > 9) {
            num = 0;
        }

        this.value = num;
        this.render();
    }

    getValue() {
        return this.value;
    }

    setPencil(num) {
        if (num < 0 || num > 9) {
            num = 0;
        }

        if (this.value != 0) {
            // 确定的值，不能做笔记
            return;
        }

        this.pencil[num] = true;
        this.render();
    }

    delPencil(num) {
        if (num < 0 || num > 9) {
            num = 0;
        }

        this.pencil[num] = false;
        this.render();
    }

    clearPencil() {
        for (var i = 0; i < 10; i++) {
            this.pencil[i] = false;
        }
        this.render();
    }

    hasPencil() {
        for (var i = 0; i < 9; i++) {
            if (this.pencil[i]) {
                return true;
            }
        }
        return false;
    }

    isPencil(num) {
        if (num < 0 || num > 9) {
            num = 0;
        }
        return this.pencil[num];
    }

    getPencil() {
        return this.pencil;
    }

    makeSure(sure) {
        this.sure = sure;
        this.render();
    }

}

